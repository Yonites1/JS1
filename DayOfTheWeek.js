let day = 2;
let dayName;

switch(day) {
    case 1:
        dayName = 'Mon';
        break;
    case 2:
        dayName = 'Tues';
        break;
    case 3:
        dayName = 'Wed';
        break;
    case 4:
        dayName = 'Thur';
        break;
    case 5:
        dayName = 'Fri';
        break;
    case 6:
        dayName = 'Sat';
        break;
    case 7:
        dayName = 'Sun';
        break;
    default:
        dayName = "Not valid input"
            
}

console.log("Day name:", dayName);

